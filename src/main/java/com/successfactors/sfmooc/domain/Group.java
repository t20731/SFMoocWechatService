package com.successfactors.sfmooc.domain;

public class Group {
    private Integer id;
    private String name;

    public Group( ) {
    }

    public Integer getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
